struct GardenGroups {

    static func solve() {
        let garden = Grid(string: input)
        let regions = findRegionsInGarden(garden)

        let s1 = regions.map { region in
            region.count * countEdgesInRegion(region)
        }.sum

        let s2 = regions.map { region in
            region.count * countSidesInRegion(region)
        }.sum

        printSolutions(s1, s2)
    }

    // Find regions by recursively aggregating together plots (grid squares)
    // where neighbours in any cardinal direction have the same plant type.
    static func findRegionsInGarden(_ garden: Grid<Character>) -> [[Vec2]] {
        var walked = garden
        return walked.size.coordinates.compactMap { coord in
            func aggregate(_ coord: Coord, plant: Character) -> [Coord] {
                guard walked[coord] == plant else { return [] }
                walked[coord] = "."
                return [coord] + coord.cardinalNeighbours().flatMap({ next in
                    aggregate(next, plant: plant)
                })

            }

            let plant = walked[coord]!
            return plant == "." ? nil : aggregate(coord, plant: plant)
        }
    }

    // For each plot find the count of cardinal neighbours that are not
    // in the region. Sum to find the total edges for the region.
    static func countEdgesInRegion(_ region: [Vec2]) -> Int {
        region.map { coord in
            coord.cardinalNeighbours().count { neighbour in
                !region.contains(neighbour)
            }
        }.sum
    }

    static func countSidesInRegion(_ region: [Vec2]) -> Int {
        // Create an index for each edge plot, to lookup the directions
        // in which that plot has an edge.
        var edgeIndex = Index<Coord, Direction>()
        region.forEach { coord in
            let directions = Direction.allCases.filter { direction in
                !region.contains(direction.advance(coord: coord))
            }

            edgeIndex.collate(directions, using: coord)
        }

        // Consume a side from the perspective of a certain direction
        // by walking the side in the two orthongal directions. Remove
        // consumed directions from the edge index to avoid double
        // counting.
        func consumeSide(_ origin: Vec2, direction: Direction) -> Int {
            func consume(_ coord: Vec2, travel: Direction) -> Int {
                guard let ds = edgeIndex[coord], ds.contains(direction) else {
                    return 0
                }

                edgeIndex[coord]!.removeAll { d in d == direction }
                return 1 + consume(coord.advanced(travel), travel: travel)
            }

            let (advance1, advance2) = direction.orthogonals
            return consume(origin, travel: advance1)
                + consume(origin.advanced(advance2), travel: advance2)
        }

        // Consider each edge plot and its edge directions.
        return edgeIndex.pairs().count { pair in
            let (coord, direction) = pair
            return consumeSide(coord, direction: direction) > 0
        }
    }

}

